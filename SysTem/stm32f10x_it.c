/*******************************************************************************
* Function Name  : RTCAlarm_IRQHandler
* Description    : This function handles RTC Alarm interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void RTCAlarm_IRQHandler(void)                               
{
	
	BSP_WDGFeedDog();
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_PWR | RCC_APB1Periph_BKP, ENABLE);		//使能PWR和BKP外设时钟   	   				RTC寄存器位于后备区域							
	PWR_BackupAccessCmd(ENABLE);													//使能后备寄存器访问	
	RTC_WaitForSynchro();															//等待RTC寄存器同步 
	RTC_WaitForLastTask();	
	if(RTC_GetFlagStatus(RTC_IT_ALR))
	{
		
		EXTI_ClearITPendingBit(EXTI_Line17);									    //清除闹钟标志为和通道EXTI17标志位
		
		if(PWR_GetFlagStatus(PWR_FLAG_WU) != RESET)      
		{	
			PWR_ClearFlag(PWR_FLAG_WU);//一般没用
		}						
		RTC_ClearITPendingBit(RTC_IT_ALR);
		RTC_WaitForLastTask();	
	}

}

